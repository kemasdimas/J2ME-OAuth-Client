package net.oauth.j2me;

public class ConsumerConfig {
    private String oauthVersion = "1.0";
    private String key;
    private String secret;
    private String callbackEndpoint;

    public ConsumerConfig() {
    }

    public ConsumerConfig(String key, String secret) {
        this.key = key;
        this.secret = secret;
    }

    public ConsumerConfig(String key, String secret, String callbackEndpoint) {
        this.key = key;
        this.secret = secret;
        this.callbackEndpoint = callbackEndpoint;
    }

    public String getOauthVersion() {
        return oauthVersion;
    }

    public void setOauthVersion(String oauthVersion) {
        this.oauthVersion = oauthVersion;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getSecret() {
        return secret;
    }

    public void setSecret(String secret) {
        this.secret = secret;
    }

    public String getCallbackEndpoint() {
        return callbackEndpoint;
    }

    public void setCallbackEndpoint(String callbackEndpoint) {
        this.callbackEndpoint = callbackEndpoint;
    }
}
